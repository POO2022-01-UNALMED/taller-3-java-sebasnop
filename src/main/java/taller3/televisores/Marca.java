package taller3.televisores;

public class Marca {
	
	// Attributes
	private String nombre;
	
	// Constructor
	public Marca (String nombre) {
		this.nombre = nombre;
	}
	
	// Getters & Setters
	public String getNombre () {
		return nombre;
	}
	public void setNombre (String nombre) {
		this.nombre = nombre;
	}
	
}
